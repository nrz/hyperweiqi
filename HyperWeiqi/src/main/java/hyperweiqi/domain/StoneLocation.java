package hyperweiqi.domain;

public class StoneLocation {

    private final int x;
    private final int y;

    public StoneLocation(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return this.x;
    }

    public int getY() {
        return this.y;
    }
}
